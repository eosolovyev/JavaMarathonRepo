package day5.task1;

public class Automobile {
    private int yearAutomobile;
    private String colorAutomobile;
    private String modelAutomobile;

    public int getYearAutomobile() {
        return yearAutomobile;
    }

    public void setYearAutomobile(int yearAuto) {
        yearAutomobile = yearAuto;
    }

    public String getColorAutomobile() {
        return colorAutomobile;
    }

    public void setColorAutomobile(String colorAuto) {
        colorAutomobile = colorAuto;
    }

    public String getModelAutomobile() {
        return modelAutomobile;
    }

    public void setModelAutomobile(String modelAuto) {
        modelAutomobile = modelAuto;
    }
}
